package com.company;

 public class Employee {
    String name, degree;
    float salary, yearsOfExperience;

    public Employee(String name, String degree, float salary, float yearsOfExperience) {
        this.name = name;
        this.degree = degree;
        this.salary = salary;
        this.yearsOfExperience = yearsOfExperience;
    }
}

